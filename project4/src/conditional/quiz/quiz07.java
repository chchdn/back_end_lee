package conditional.quiz;

public class quiz07 {
//  b - 새
//  c - 고양이
//  d - 강아지
	public static void main(String[] args) {
		char animal = 'd';

		switch (animal) {
		case 'b':
			System.out.println("새 귀여움");
			break;
		case 'c':
			System.out.println("고양이 귀여움");
		case 'd':
			System.out.println("강아지 짱귀여움");
			break;
		}

	}

}

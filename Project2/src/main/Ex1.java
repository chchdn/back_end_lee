package main;

public class Ex1 {

	public static void main(String[] args) {

		int x; //  정수형 변수 선언
		x = 10; // 변수에 값 10을 대입
		
		// "sysout" 작성 > ctrl + space > 프린트 함수가 자동완성 기능 문구들이 나옴
		System.out.println(x);
		System.out.println("안녕하세요");
		System.out.println("반갑습니다"); // println 함수는 출력 후 줄바꿈
		System.out.print("반갑십니더"); // print 함수는 줄바꿈 없음
	}

}
